            string answer = Device.QueryLine(GetCommandForErrorQuery());
            int.TryParse(answer, out var result);
            if (Enum.IsDefined(typeof(ErrorCode5720A), result))
                return result;
            return (int)ErrorCode5720A.NoError;
        }

       

